<script>
    // Carousel control for mobile
    if ($(window).width() < 700){
        $(".carousel").on("touchstart", function(event){
                var xClick = event.originalEvent.touches[0].pageX;
            $(this).one("touchmove", function(event){
                var xMove = event.originalEvent.touches[0].pageX;
                if( Math.floor(xClick - xMove) > 5 ){
                    $(this).carousel('next');
                }
                else if( Math.floor(xClick - xMove) < -5 ){
                    $(this).carousel('prev');
                }
            });
            $(".carousel").on("touchend", function(){
                    $(this).off("touchmove");
            });
        });
    }
    
    // scaling product images
    $(".grt-product").css("width","50%");

    // sliding
    (function($) {
        $('#carousel').on('slide.bs.carousel', function (e) {
        var $e = $(e.relatedTarget);
        var $t = $(this);
        var $inner = $t.find('.carousel-inner');
        var idx = $e.index();
        var itemsPerSlide = 3;
        var totalItems = $t.find('.carousel-item').length;

        if (idx >= totalItems-(itemsPerSlide-1)) {
            var it = itemsPerSlide - (totalItems - idx);
            for (var i=0; i<it; i++) {
                // append slides to end
                if (e.direction=="left") {
                    $t.find('.carousel-item').eq(i).appendTo($inner);
                }
                else {
                    $t.find('.carousel-item').eq(0).appendTo($inner);
                }
            }
        }
        });
    })(jQuery);
</script>