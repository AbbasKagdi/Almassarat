      <p class="">Hardened conventional cement paste has mainly an agglomerated structure of calcium silicate hydrates and calcium hydroxide bound together by the weak intermolecular forces. Consequently, microcracks occur easily in the cured material under stress. That is why ordinary cement mortar and concrete have poor tensile strength and fracture toughness. On the other side, in the polymer modified cement, it appears that the microcracks are bridged by the polymer films which prevent crack propagation, and simultaneously, a strong cement hydrate-aggregate bond is developed.</p>
      <p class="">PCM also has a sealing effect to the formed structure and thus provide a considerable increase in toughness, waterproofing characteristics, resistance to chemical penetration, and freeze-thaw durability. Depending on traffic volumes, gravel characteristics and environmental conditions at the time, GRT’s PCM blend pavements can generally be trafficked immediately after final compaction; and once cured, provides a durable temporary running surface for construction traffic without the risk of suffering from degree of saturation failures that occur with unbound gravels. On larger projects, we can also formulate our additive to meet project specific requirements with regard to setting times.</p>
    </div>
    <div class="col-md-6 small">
      <p class="">A simplified description of the process for the creation of polymer modified cement is outlined below: After mixing with water, the cement paste particles start to hydrate and cement gel gradually forms on the surface of the cement particles. The unhydrated cement particles covered with cement gel is being enveloped with a closely packed layer of introduced polymer particles. The water removes by cement hydration and evaporation, and the closely packed polymer particles, positioned on the cement hydrates, integrate into continuous membranes. The membranes bind the cement hydrates together to form a monolithic network with the polymer providing increased flexibility and providing an improved barrier against uncontrolled drying of cement particles.</p>
      <p class="">The binder should be added in sufficient quantity to balance both strength and shrinkage requirements to produce a bound material with significant toughness and fatigue strength to carry the design traffic loading. In most cases, cementitious contents for GRT polymer mix designs are limited to 2 to 3%. Even at these low dosages, we have found 28 day UCS results for some gravels to be in excess of 5MPa. Our testing has also found that depending on the gravel characteristics, the results achieved by GRT PCM with cement exceeds that sum of individual UCS test results of the cement and polymer alone thereby indicating that cross-linking of the cementitious binder and polymer has occurred. Our testing have also found that the PCM acts in a similar way to a water reducing agent in structural concrete.</p>
