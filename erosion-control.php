  <h5 class="orange mt-5">Impacts of Erosion in Infrastructure</h5>
  <p class="lead">Most of the soils have some sodic properties. Sodic soil is the term given to soil that holds sodium – a cation (a positive ion) – that is held loosely on clay particles in the soil.</p>
  <p>High sodicity in soil causes clay to swell when it’s wet, and the clay particles move so far apart that they disperse. This, in turn, weakens the aggregate in the soil, causing collapses, and closing off soil pores. When this happens, water and air movement in sodic soils is severely limited and causes structural issues.</p>
  <p>Sodic soils are particularly vulnerable to most forms of soil erosion and can affect the infrastructure of roads. Soil stabilisation is a vital component of successful road construction, and considering bitumen and concrete produce a lot of runoff, necessary steps need to be taken with stabilising the soil in road construction.</p>
  <p>In mining and exploration activities, the natural drainage of the land is interfered with as roads are built and digs undertaken. Disturbing the soil can cause environmental issues like erosion and silting of waterways. Mining exploration requires the development of hundreds of kilometres of roads and tracks, and because of this, soil stability and erosion prevention is a vital component of ensuring environmentally safe mining works.</p>

