      <h5 class="text-primary mt-5" id="cavitation">Ultrasound & Cavitation Process</h5>
      <p>Ultrasounds are sound waves with frequencies above the human auditory range (20 – 100 kHz). The ultrasound applications are based on the physical phenomenon of cavitation which may be induced in any liquid medium.The sound waves cause the microscopic bubbles present in the natural liquids to expand (during phases A and B of low pressure) and contract until they implode (during phases C and D of high pressure). The formation and implosion of bubbles (cavitation process) is produced thousands of times per second.</p>
      
